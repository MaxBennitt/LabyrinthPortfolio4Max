class SplashScreen {
    constructor() {
        this.currentFrame = 0;
        this.isDirty = true;
        this.frameTimer = 0;
        this.frameDelay = 1000;
    }

    update() {
        this.frameTimer += 250;
        if (this.currentFrame < outputGraphics.length - 1) {
            this.currentFrame++;
            this.isDirty = true;
        }
        this.frameTimer = 0;
    }

    draw() {
        if (this.isDirty) {
            console.log(ANSI.CLEAR_SCREEN, ANSI.CURSOR_HOME);
            console.log(outputGraphics[this.currentFrame]);
            this.isDirty = false;
        }
    }

    isFinished() {
        return this.currentFrame >= outputGraphics.length - 1;
    }
}

export default SplashScreen;